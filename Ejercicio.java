public class Ejercicio {
    //imprimir lo n primeros multiplos de un num dado
    public static void main(String[] args) {
        final int N = 3;
        final int NUM = 7;

        for (int i=1; i<=N; i++ ) {
            System.out.println("Los tres primeros son :" + NUM*i );
        }
    
    }   
}